def test_19(df_movies3, df_movies_solution):
    print("Testing: ", end="")
    assert type(df_movies3["genres"]) is Series
    assert all(isinstance(genre, set) for genre in df_movies3["genres"])

    partial_solution = Series(
        [{'Fantasy', 'Animation', 'Children', 'Adventure', 'Comedy'},
         {'Children', 'Fantasy', 'Adventure'}, {'Comedy', 'Romance'},
         {'Comedy', 'Romance', 'Drama'}, {'Comedy'}],
        index=[0, 1, 2, 3, 4]).astype("object")

    # Msgpack does not support sets, so we used a list. We now transform that list into a set.
    partial_solution = partial_solution.map(set)

    pd.testing.assert_series_equal(df_movies3["genres"].head(),
                                   partial_solution, check_names=False)

    df_movies_solution["genres"] = df_movies_solution["genres"].map(
        lambda genre: set(genre.split("|")) if isinstance(genre,
                                                          str) else genre
    )
    pd.testing.assert_series_equal(df_movies3["genres"],
                                   df_movies_solution["genres"])

    print("success!")


